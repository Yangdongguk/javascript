// 조건 - if/else-if/else
// 조건 - switch

const season = "spring";

switch (season) {
  case "spring":
    console.log("봄이군요");
    break;
  case "summer":
    console.log("여름이군요");
    break;
  case "fall":
    console.log("가을이군요");
    break;
  case "winter":
    console.log("겨울이군요");
    break;
  default:
    console.log("알 수 없군요");
    break;
}
